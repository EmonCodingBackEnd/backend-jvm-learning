package com.coding.jvm07.gui.nurse;

/**
 * 需要打疫苗的人员
 */
public class People {
    /**
     * 人员信息
     */
    private String address;

    /**
     * 人员年龄
     */
    private String num;

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getNum() {
        return num;
    }

    public void setNum(String num) {
        this.num = num;
    }
}
